#include "push_swap.h"

int	is_sort(t_stack **a)
{
	t_stack	*tmp;

	tmp = (*a);
	while (tmp->next)
	{
		if (tmp->nb > tmp->next->nb)
			return (0);
		tmp = tmp->next;
	}
	return (1);
}
